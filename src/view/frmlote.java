    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String numeroLote = txtNumeroLote.getText().trim();
        Date fechaVencimiento = dcFechaVencimiento.getDate();
        int cantidad = Integer.parseInt(txtCantidad.getText());
        String productoSeleccionado = (String) cbProducto.getSelectedItem();

        if (numeroLote.isEmpty() || fechaVencimiento == null || productoSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos");
            return;
        }

        producto p = mapaProductos.get(productoSeleccionado);
        if (p == null) {
            JOptionPane.showMessageDialog(this, "Producto no encontrado");
            return;
        }

        lote l = new lote();
        l.setNumeroLote(numeroLote);
        l.setFechaVencimiento(fechaVencimiento);
        l.setCantidad(cantidad);
        l.setFechaIngreso(new Date());
        l.setIdProducto(p.getIdProducto()); // ✅ Solo guarda el ID
        l.setIdUsuario(usuarioLogueado.getIdUsuario());

        loteController controller = new loteController();
        boolean exito = controller.registrarLote(l);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Lote registrado correctamente");
            limpiarFormulario();
            cargarLotes();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar el lote");
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnAnularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnularActionPerformed
        // TODO add your handling code here:
        int fila = tblLotes.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un lote");
            return;
        }

        int idLote = (int) tblLotes.getValueAt(fila, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "¿Anular este lote?");
        if (confirm == JOptionPane.YES_OPTION) {
            loteController controller = new loteController();
            boolean exito = controller.anularLote(idLote);
            if (exito) {
                JOptionPane.showMessageDialog(this, "Lote anulado");
                cargarLotes();
            }
        }
        
    }//GEN-LAST:event_btnAnularActionPerformed

