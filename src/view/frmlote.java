        private lote obtenerLotePorId(int idLote) {
        loteController controller = new loteController();
        List<lote> lotes = controller.obtenerLotes();
        for (lote l : lotes) {
            if (l.getIdLote() == idLote) {
                return l;
            }
        }
        return null;
    }

    private producto encontrarProductoPorId(int idProducto) {
        for (producto p : mapaProductos.values()) {
            if (p.getIdProducto() == idProducto) {
                return p;
            }
        }
        return null;
    }
    private void tblLotesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLotesMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2 && !evt.isConsumed()) {
            evt.consume();
            int fila = tblLotes.getSelectedRow();
            if (fila != -1) {
                int idLote = (int) tblLotes.getValueAt(fila, 0);
                lote l = obtenerLotePorId(idLote);
                if (l != null) {
                    txtNumeroLote.setText(l.getNumeroLote());
                    dcFechaVencimiento.setDate(l.getFechaVencimiento());
                    txtCantidad.setText(String.valueOf(l.getCantidad()));

                    // Buscar producto por idProducto y seleccionarlo en el combo
                    producto prod = encontrarProductoPorId(l.getIdProducto());
                    if (prod != null) {
                        String displayText = prod.getSku() + " - " + prod.getNombre();
                        cbProducto.setSelectedItem(displayText);
                        txtSKU.setText(prod.getSku());
                        txtCodigoBarras.setText(prod.getCodigoBarras());
                    }

                    loteSeleccionado = l.getIdLote();
                }
            }
        }
    }//GEN-LAST:event_tblLotesMouseClicked


