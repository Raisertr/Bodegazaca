     private void cargarProductos() {
        cbProducto.removeAllItems();
        mapaProductos.clear();

        productoController controller = new productoController();
        List<producto> productos = controller.obtenerProductos();

        for (producto p : productos) {
            String displayText = p.getSku() + " - " + p.getNombre();
            cbProducto.addItem(displayText);
            mapaProductos.put(displayText, p); // Guarda relación entre texto y objeto
        }
    }
     
     private void cargarLotes() {
        DefaultTableModel modelo = (DefaultTableModel) tblLotes.getModel();
        modelo.setRowCount(0);

        loteController controller = new loteController();
        List<lote> lotes = controller.obtenerLotes();

        for (lote l : lotes) {
            Object[] fila = {l.getIdLote(), l.getNumeroLote(), l.getFechaVencimiento(), l.getCantidad(),  l.getIdProducto()};
            modelo.addRow(fila);
        }
    }
     
